i2cset -f -y 16 0x3f 0x0a 0x1e
i2cset -f -y 16 0x3f 0x0d 0x01
i2cset -f -y 16 0x3f 0x16 0x40
i2cset -f -y 16 0x3f 0x00 0x01
i2cset -f -y 16 0x3f 0x17 0xc8
i2cset -f -y 16 0x3f 0x00 0x04
i2cset -f -y 16 0x3f 0x30 0x00
i2cset -f -y 16 0x3f 0x31 0x00
i2cset -f -y 16 0x3f 0x32 0x00
i2cset -f -y 16 0x3f 0x33 0x01
i2cset -f -y 16 0x3f 0x00 0x08
i2cset -f -y 16 0x3f 0x18 0x00
i2cset -f -y 16 0x3f 0x19 0x00
i2cset -f -y 16 0x3f 0x1a 0x00
