#!/bin/bash
# Simple Audio Fix for Lenovo Yoga Pro 9i Gen 10 (83L0)
# Usage: sudo ~/.config/scripts/lenovo/audio-fix-simple.sh

# Load I2C module
modprobe i2c-dev

# Configure TAS2781 amplifiers on I2C bus 16
echo "Configuring audio amplifiers..."

# Address 0x3f (Left channel)
i2cset -f -y 16 0x3f 0x00 0x00
i2cset -f -y 16 0x3f 0x7f 0x00
