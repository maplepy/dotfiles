i2cset -f -y 16 0x38 0x29 0x00
i2cset -f -y 16 0x38 0x2a 0x00
i2cset -f -y 16 0x38 0x2b 0x00
i2cset -f -y 16 0x38 0x00 0x0a
i2cset -f -y 16 0x38 0x48 0x00
i2cset -f -y 16 0x38 0x49 0x00
i2cset -f -y 16 0x38 0x4a 0x00
i2cset -f -y 16 0x38 0x4b 0x00
i2cset -f -y 16 0x38 0x58 0x40
i2cset -f -y 16 0x38 0x59 0x00
i2cset -f -y 16 0x38 0x5a 0x00
