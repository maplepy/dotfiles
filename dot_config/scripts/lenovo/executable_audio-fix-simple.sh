i2cset -f -y 16 0x3f 0x28 0x40
i2cset -f -y 16 0x3f 0x29 0x00
i2cset -f -y 16 0x3f 0x2a 0x00
i2cset -f -y 16 0x3f 0x2b 0x00
i2cset -f -y 16 0x3f 0x00 0x0a
i2cset -f -y 16 0x3f 0x48 0x00
i2cset -f -y 16 0x3f 0x49 0x00
i2cset -f -y 16 0x3f 0x4a 0x00
i2cset -f -y 16 0x3f 0x4b 0x00
i2cset -f -y 16 0x3f 0x58 0x40
i2cset -f -y 16 0x3f 0x59 0x00
i2cset -f -y 16 0x3f 0x5a 0x00
i2cset -f -y 16 0x3f 0x5b 0x00
i2cset -f -y 16 0x3f 0x00 0x00
i2cset -f -y 16 0x3f 0x02 0x00

# Address 0x38 (Right channel)
i2cset -f -y 16 0x38 0x00 0x00
i2cset -f -y 16 0x38 0x7f 0x00
i2cset -f -y 16 0x38 0x01 0x01
i2cset -f -y 16 0x38 0x0e 0xc4
i2cset -f -y 16 0x38 0x0f 0x40
i2cset -f -y 16 0x38 0x5c 0xd9
i2cset -f -y 16 0x38 0x60 0x10
i2cset -f -y 16 0x38 0x0a 0x2e
i2cset -f -y 16 0x38 0x0d 0x01
i2cset -f -y 16 0x38 0x16 0x40
i2cset -f -y 16 0x38 0x00 0x01
i2cset -f -y 16 0x38 0x17 0xc8
i2cset -f -y 16 0x38 0x00 0x04
i2cset -f -y 16 0x38 0x30 0x00
i2cset -f -y 16 0x38 0x31 0x00
i2cset -f -y 16 0x38 0x32 0x00
i2cset -f -y 16 0x38 0x33 0x01
i2cset -f -y 16 0x38 0x00 0x08
i2cset -f -y 16 0x38 0x18 0x00
